package com.example.javaproject2.codeup;

import java.util.Scanner;

public class Codeup1074 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int iVal = sc.nextInt();

        for (int i = iVal; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
